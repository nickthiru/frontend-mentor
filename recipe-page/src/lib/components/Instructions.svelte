<section id="instructions-section" class="instructions" aria-labelledby="instructions">
  <h2 id="instructions" class="text-preset-2">Instructions</h2>
  <ol>
    <li>
      <span class="inc-indent"><span class="text-preset-4-bold">Beat the eggs</span><span
          class="text-preset-4">: In a bowl, beat the eggs
          with a pinch of salt and pepper until they are well mixed. You can add a tablespoon of water or milk
          for
          a fluffier texture.</span></span>
    </li>
    <li>
      <span class="inc-indent"><span class="text-preset-4-bold">Heat the pan</span><span class="text-preset-4">:
          Place a non-stick frying
          pan over medium heat and add butter or oil.</span></span>
    </li>
    <li>
      <span class="inc-indent"><span class="text-preset-4-bold">Cook the omelette</span><span
          class="text-preset-4">: Once the butter
          is melted and bubbling, pour in the eggs. Tilt the pan to ensure the eggs evenly coat the
          surface.</span></span>
    </li>
    <li>
      <span class="inc-indent"><span class="text-preset-4-bold">Add fillings (optional)</span><span
          class="text-preset-4">: When the
          eggs begin to set at the edges but are still slightly runny in the middle, sprinkle your chosen
          fillings
          over one half of the omelette.</span></span>
    </li>
    <li>
      <span class="inc-indent"><span class="text-preset-4-bold">Fold and serve</span><span
          class="text-preset-4">: As the omelette
          continues to cook, carefully lift one edge and fold it over the fillings. Let it cook for another
          minute,
          then slide it onto a plate.</span></span>
    </li>
    <li>
      <span class="inc-indent"><span class="text-preset-4-bold">Enjoy</span><span class="text-preset-4">: Serve
          hot, with additional
          salt and pepper if needed.</span></span>
    </li>
  </ol>
</section>